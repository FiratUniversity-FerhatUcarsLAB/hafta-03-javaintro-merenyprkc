public class MilKilometreTablosu {
    public static void main(String[] args) {
        double milKilometre = 1.609;
        int mil1 = 1;
        int mil2 = 5;
        int mil3 = 10;
        int mil4 = 20;
        int mil5 = 50;
        
        System.out.println("Mil       Kilometre");
        System.out.println("--------------------");
        System.out.println(mil1 + "         " + (mil1 * milKilometre));
        System.out.println(mil2 + "         " + (mil2 * milKilometre));
        System.out.println(mil3 + "        " + (mil3 * milKilometre));
        System.out.println(mil4 + "        " + (mil4 * milKilometre));
        System.out.println(mil5 + "        " + (mil5 * milKilometre));
    }
}
