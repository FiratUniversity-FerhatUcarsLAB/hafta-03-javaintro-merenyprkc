public class Faktoriyel {
    public static void main(String[] args) {
        System.out.println("5! Hesaplama Adımları:");
        int faktoriyel = 1*2;
        System.out.println("  2!= " + faktoriyel);
        faktoriyel = faktoriyel*3;
        System.out.println("  3!= " + faktoriyel);
        faktoriyel = faktoriyel*4;
        System.out.println("  4!= " + faktoriyel);
        faktoriyel = faktoriyel*5;
        System.out.println("  5!= " + faktoriyel);
    }
}
