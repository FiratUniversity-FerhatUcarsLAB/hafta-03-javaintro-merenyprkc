public class DaireHesap {
    public static void main(String[] args) {
        double r = 5.5;
        double pi = 3.14159;

        double alan = pi * r * r;
        double cevre = 2 * pi * r;

        System.out.println("Yarıçap: " + r);
        System.out.println("Alanı: " + alan);
        System.out.println("Çevresi: " + cevre);
    }
}
